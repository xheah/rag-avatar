# 3. Barge-in via injected WebSocket message (voice_page fixture)
# ─────────────────────────────────────────────────────────────────────────────

class TestBargeInViaWebSocket:
    """
    These tests verify the barge-in interrupt pipeline by injecting a
    'barge_in' WebSocket message directly from JavaScript.  This approach is
    more reliable than depending on Silero VAD detecting a synthetic tone in
    a headless browser and exercises exactly the same code path that
    onSpeechStart triggers.

    The voice_page fixture is used because it also has a fake mic device
    which prevents getUserMedia from prompting.
    """

    def test_barge_in_stops_speaking_state(self, voice_page: Page):
        """
        Core barge-in test.

        Flow:
          1. Activate voice mode (need_vad=False — button flipped is enough)
          2. Send a text message → avatar enters 'Speaking…'
          3. Inject a barge_in over the control WS from JS
          4. Avatar must leave 'Speaking…' and return to 'Listening…'
        """
        page = voice_page
        expose_ws_instances(page)
        page.reload(wait_until="domcontentloaded")
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "Explain objection handling in detail please")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        # Inject barge_in via JS — same effect as onSpeechStart firing
        inject_barge_in(page)

        # Status must leave Speaking… quickly
        expect(page.get_by_text("Speaking…")).not_to_be_visible(timeout=5_000)

    def test_app_accepts_new_message_after_injected_barge_in(self, voice_page: Page):
        """
        After a barge-in the isBusyRef lock must be released (Fix #1).
        """
        page = voice_page
        expose_ws_instances(page)
        page.reload(wait_until="domcontentloaded")
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "First question to be interrupted")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        inject_barge_in(page)
        expect(page.get_by_text("Speaking…")).not_to_be_visible(timeout=5_000)

        # isBusyRef is now false — next send must be accepted
        type_and_send(page, "Second question after barge-in")
        expect(
            page.get_by_text("Second question after barge-in")
        ).to_be_visible(timeout=10_000)

    def test_no_stale_audio_after_injected_barge_in(self, voice_page: Page):
        """
        After barge-in, stale audio must NOT resume.
        We verify this by confirming 'Speaking…' does NOT reappear
        within 2 s of the interrupt landing.
        """
        page = voice_page
        expose_ws_instances(page)
        page.reload(wait_until="domcontentloaded")
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "Long response please")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        inject_barge_in(page)
        expect(page.get_by_text("Speaking…")).not_to_be_visible(timeout=5_000)

        # Stale audio would restart Speaking… within a second
        page.wait_for_timeout(2_000)
        expect(page.get_by_text("Speaking…")).not_to_be_visible(timeout=1_000)


