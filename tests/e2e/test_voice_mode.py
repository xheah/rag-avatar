class TestVoiceModeWithTextInput:
    """
    Even with voice mode active, text input must work.
    These tests activate voice mode then use the keyboard — they do NOT need
    VAD to finish loading because they don't exercise the mic pipeline at all.

    Root cause of previous failure: activate_voice_mode() waited for
    "Listening…" which requires the ONNX model to load (can take > 10 s in
    headless Chrome).  Fixed by using need_vad=False (default) so we only
    wait for the 'Active' button state.
    """

    def test_text_send_works_while_voice_mode_active(self, text_page: Page):
        page = text_page
        # need_vad=False: just confirm the button flipped, no ONNX wait
        activate_voice_mode(page, need_vad=False)

        type_and_send(page, "Text send in voice mode")
        expect(page.get_by_text("Text send in voice mode")).to_be_visible(
            timeout=10_000
        )

    def test_status_shows_analyzing_after_send_in_voice_mode(self, text_page: Page):
        page = text_page
        activate_voice_mode(page, need_vad=False)

        type_and_send(page, "Quick question in voice mode")
        # 'Analyzing…' is set synchronously inside handleSend — appears fast
        expect(page.get_by_text("Analyzing…")).to_be_visible(timeout=10_000)

    def test_input_cleared_after_send_in_voice_mode(self, text_page: Page):
        """Input field must be cleared after send even in voice mode."""
        page = text_page
        activate_voice_mode(page, need_vad=False)
        type_and_send(page, "Voice mode text clear test")
        page.wait_for_function(
            f"() => document.querySelector(\"{INPUT_SEL}\")?.value === ''",
            timeout=5_000,
        )


