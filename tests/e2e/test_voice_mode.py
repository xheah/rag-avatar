@pytest.mark.vad
class TestBargeInViaVAD:
    """
    Best-effort tests that rely on Silero VAD detecting the 440 Hz sine tone.
    Marked with @pytest.mark.vad so they can be skipped in CI:
      pytest -m "not vad"

    These are skipped automatically if the voice_page fixture skips
    (WAV file not found).
    """

    def test_avatar_transitions_from_speaking_to_listening_via_vad(
        self, voice_page: Page
    ):
        """
        Full VAD path: fake mic tone triggers onSpeechStart which fires the
        barge-in without any JS injection.
        """
        page = voice_page
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "Explain objection handling in detail please")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        # Looping 440 Hz tone → VAD onSpeechStart → interruptAudio + barge_in
        expect(page.get_by_text("Listening…")).to_be_visible(timeout=20_000)
        expect(page.get_by_text("Speaking…")).not_to_be_visible(timeout=3_000)


