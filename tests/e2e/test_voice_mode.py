# 5. Silence baseline (silence_page fixture — WAV required)
# ─────────────────────────────────────────────────────────────────────────────

class TestSilenceBaseline:
    """
    Uses silence.wav as the mic input.  VAD should NOT fire, so the avatar
    should complete a full response without interruption.
    """

    def test_response_completes_without_barge_in_on_silence(self, silence_page: Page):
        """
        With a silent mic no barge-in should fire.  The avatar must transition
        Speaking… → Online after audio finishes naturally.

        Root cause of previous failure: activate_voice_mode timed out before
        VAD loaded (same ONNX issue).  Fixed by need_vad=True + 90 s timeout.
        """
        page = silence_page
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "Brief greeting response please")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        # After audio finishes naturally, status returns to Listening… or Online
        page.wait_for_function(
            "() => !document.body.textContent.includes('Speaking…')",
            timeout=60_000,
        )

    def test_silence_does_not_trigger_barge_in(self, silence_page: Page):
        """
        Silence must not trigger onSpeechStart — the speaking state must
        persist until audio finishes (no premature Listening… flash).
        We confirm by checking that 'Listening…' does NOT appear in the
        first 3 s after Speaking… begins.
        """
        page = silence_page
        activate_voice_mode(page, need_vad=True, timeout=90_000)

        type_and_send(page, "Tell me something brief")
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=40_000)

        # Silence → no barge-in → 'Listening…' must NOT appear for at least 3 s
        page.wait_for_timeout(3_000)
        # If Listening… appeared it means VAD fired (barge-in happened) — that's wrong
        expect(page.get_by_text("Speaking…")).to_be_visible(timeout=1_000)
