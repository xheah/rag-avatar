"""
tests/e2e/test_voice_mode.py
-----------------------------
Layer 6 — E2E tests for voice mode and barge-in behaviour.

These tests require:
  1. A running backend:  uvicorn src.api:app --port 8000
  2. A running frontend: cd frontend && npm run dev
  3. WAV fixture files generated by: python tests/e2e/fixtures/generate_wavs.py
  4. Valid CARTESIA_API_KEY + DEEPGRAM_API_KEY in .env (for full voice path)

How the fake mic works:
  Playwright launches Chromium with --use-fake-device-for-media-stream and
  --use-file-for-fake-audio-capture=<wav>.  The browser's MediaDevices API
  then streams the WAV audio in a loop to any getUserMedia() call, including
  the WebRTC track that feeds Deepgram.

Marks
  @pytest.mark.e2e      — real browser required
  @pytest.mark.voice    — additionally requires WAV fixtures + API keys

Root-cause notes (from first-run failures)
  ① activate_voice_mode() was waiting for "Listening…" with only a 10-12 s
    timeout.  Silero VAD loads a 1.8 MB ONNX model via fetch(), then compiles
    it through WASM in headless Chrome — this routinely takes 20-60 s.
    Fix: use a 90 s timeout and only require "Active" for tests that don't
    actually need the VAD running.

  ② Barge-in tests need Silero VAD to detect the 440 Hz sine.  Because the
    fake audio device loops, the tone is continuously present — which actually
    PREVENTS the model from detecting a *rising edge* (speech_start).
    Fix: instead of relying on the VAD event chain for assertion, inject a
    direct barge_in via JavaScript through the WebSocket so the test is
    decoupled from ONNX timing.  The VAD pipe tests remain as a best-effort
    check gated on a separate `vad_ok` mark.
"""

import os
import pytest
from playwright.sync_api import Page, expect

pytestmark = [pytest.mark.e2e, pytest.mark.voice]

INPUT_SEL  = "input[placeholder='Type here...']"
VOICE_SEL  = "button:has-text('Voice Mode')"
ACTIVE_SEL = "button:has-text('Active')"

# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────

def type_and_send(page: Page, text: str) -> None:
    """Fill the text input and press Enter (works even in voice mode)."""
    page.locator(INPUT_SEL).fill(text)
    page.keyboard.press("Enter")


def wait_for_online(page: Page, timeout: int = 90_000) -> None:
    """
    Wait until orbState returns to 'idle' (status label 'Online').
    This is the only reliable signal that isBusyRef has been released.
    """
    page.wait_for_function(
        "() => document.body.textContent.includes('Online')", timeout=timeout
    )


def activate_voice_mode(page: Page, *, need_vad: bool = False,
                         timeout: int = 90_000) -> None:
    """
    Click the Voice Mode toggle and wait for the button to show 'Active'.

    By default (need_vad=False) we only assert that `voiceModeActive` flipped,
    which happens synchronously.  This is sufficient for text-send tests.

    When need_vad=True we additionally wait for "Listening…" which confirms
    that MicVAD finished loading the ONNX model.  Use the 90 s default timeout
    because headless Chrome's WASM JIT compilation is slow.
    """
    page.locator(VOICE_SEL).click()
    expect(page.get_by_text("Active")).to_be_visible(timeout=10_000)
    if need_vad:
        expect(page.get_by_text("Listening…")).to_be_visible(timeout=timeout)


def inject_barge_in(page: Page) -> None:
    """
    Fire a barge-in directly via the browser's WebSocket by calling the same
    code path that onSpeechStart uses.  This lets us test the interrupt
    pipeline without depending on Silero VAD detecting a synthetic sine tone.
    """
    page.evaluate("""
        () => {
            // Find the control WebSocket on the closed-over ref — we reach it
            // through the global window.__controlWs if the app exposes it,
            // otherwise we trigger the JS-visible barge-in path directly.
            const wsProto = window.WebSocket.prototype;
            const instances = window.__controlWsInstances || [];
            if (instances.length > 0) {
                const ws = instances[instances.length - 1];
                if (ws && ws.readyState === WebSocket.OPEN) {
                    ws.send(JSON.stringify({ type: 'barge_in' }));
                }
            }
        }
    """)


def expose_ws_instances(page: Page) -> None:
    """
    Patch the page so every new WebSocket instance is recorded on
    window.__controlWsInstances.  Call this BEFORE page.goto().
    We add it as an init script via the conftest, but here as a fallback.
    """
    page.add_init_script("""
        window.__controlWsInstances = [];
        const _OrigWS = window.WebSocket;
        window.WebSocket = function(...args) {
            const ws = new _OrigWS(...args);
            window.__controlWsInstances.push(ws);
            return ws;
        };
        Object.assign(window.WebSocket, _OrigWS);
    """)


# ─────────────────────────────────────────────────────────────────────────────
# 1. Voice mode toggle (text_page fixture — no WAV required)
# ─────────────────────────────────────────────────────────────────────────────

class TestVoiceModeToggle:
    """These tests use text_page — no WAV fixtures needed."""

    def test_voice_mode_button_exists(self, text_page: Page):
        expect(text_page.locator(VOICE_SEL)).to_be_visible(timeout=5_000)

    def test_voice_mode_button_shows_active_state_on_click(self, text_page: Page):
        """
        Clicking Voice Mode should flip voiceModeActive → true, which changes
        the button label to 'Active' synchronously via React state.
        """
        text_page.locator(VOICE_SEL).click()
        # 'Active' appears as soon as setVoiceModeActive(true) is called —
        # this is synchronous, no need to wait for VAD or WebRTC.
        expect(text_page.get_by_text("Active")).to_be_visible(timeout=10_000)

    def test_voice_mode_can_be_toggled_off(self, text_page: Page):
        """Clicking again should deactivate voice mode (button returns to 'Voice Mode' label)."""
        page = text_page
        page.locator(VOICE_SEL).click()
        expect(page.get_by_text("Active")).to_be_visible(timeout=10_000)
        # The 'Active' button IS the toggle button — click it to turn off
        page.locator(ACTIVE_SEL).click()
        expect(page.locator(VOICE_SEL)).to_be_visible(timeout=5_000)

    def test_orb_state_starts_as_online(self, text_page: Page):
        """Before voice mode, the status label is 'Online'."""
        expect(text_page.get_by_text("Online")).to_be_visible(timeout=5_000)


